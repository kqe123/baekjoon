# 초기 상태에서 위와 같은 과정을 한 번 거치면 총 4개의 정사각형이 새로 생긴다.
# 이와 같은 과정을 상근이가 만족할 때 까지 계속한다.
# 상근이는 어떤 점은 한 개 보다 많은 정사각형에 포함될 수 있다는 사실을 알았다. 
# 메모리 소모량을 줄이기 위해서 중복하는 점을 한 번만 저장하려고 한다. 과정을 N번 거친 후 점 몇 개를 저장해야 하는지 구하는 프로그램을 작성하시오.

N = int(input())
value = 4
side_num = 2
for i in range(1, N+1) : 
    side_num = 2*side_num - 1
    value = side_num**2

print(value)